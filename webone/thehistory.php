<h2>The Beginnings of Swarovski</h2>
<p style="padding:0 10px 0 10px;
font-size:16px;">
In 1862, Daniel Swarovski was born in Bohemia to a family that owned a crystal-cutting company.
 Now part of the Czech Republic, the Bohemia region has a rich history of crystal crafsmanship.
  In 1883, Daniel formed a jewelry company and by 1886, they had over 70 employees, 
  and supplied jewelry for Queen Victoria. In 1891, Daniel registered a patent for the crystal
   cutting machine that he created and in 1895, moved from Bohemia to Wattens, Austria to begin crystal production.<p>
