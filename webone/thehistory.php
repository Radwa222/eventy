<h2>The Fashion Boom</h2>
<div style=""><p style="float:left;
font-size:16px;
width:370px; 
margin:40px 10px 0px 200px;">
In the years following World War II, fashion became a huge trend. Popular designers such as Coco Chanel,
 Christian Dior and other Hollywood designers snapped up Swarovski crystal designs. In 1956, Daniel Swarovski 
 collaborated with Christian Dior to create the popular Aurora Borealis (AB) effect that is still used to this
  day on crystal colors. During this era, Hollywood designers started using Swarovski Crystals en masse. Marilyn Monroe wore crystal
   in "Gentlemen Prefer Blondes" and during her famous birthday song to John F Kennedy. Swarovski Crystals were
    also used for the Ruby Slippers in "The Wizard of Oz" and they made appearances in "Sabrina" and
     "Breakfast at Tiffany's." In 1974, the company developed the Hotfix technology allowing crystals to be fixed
      directly to clothing and fabrics without metal or plastic settings. In 1989, Swarovski replaced its original
       Edelweiss logo with the Swan logo, which is used on Swarovski packaging and storefronts around the world. </p>
