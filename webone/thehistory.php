<h2>Adapting to Trends</h2>
<p style="padding:0 10px 0 10px;
font-size:16px;">In 1911, Daniel Swarovski and his family built
 their own glassworks factory and produced premium quality glass. During the 1920's, the "flapper" fashion trend 
 caused a surge in demand for fashionable Swarovski designs. Shorter women's hairstyles led Swarovski to create
  a crystal-embellished fabric headband, which was also used as trimming for clothing. After the end of World War II,
   the company founded Swarovski Optik which used its expertise in glass manufacturing to create lenses, binoculars,
    and telescopes, making Swarovski a market leader in precision optics.<p>
<img src="h3.jpg" style="padding:10px" alt="The Swarovski Crystal Headquarters in Wattens, Austria" 
